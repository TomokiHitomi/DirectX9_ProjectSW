	static Prop m_tagProp;
public:
	enum CUBE
	{	// プレイヤー管理
		CUBE_MAIN,
		CUBE_MAX
	};

	// コンストラクタ（初期化処理）
	Cube(void);
	//デストラクタ（終了処理）
	~Cube(void);

	// 更新処理
	void	Update(void);
	// 描画処理
	void	Draw(void);

	static void SetPos(D3DXVECTOR3 vPos) { m_tagProp.vPos = vPos; }
